fun main() {
    //contoh kode menggunakan var
    var varVariabel: Int
    varVariabel = 56 //assigning first value to varVariabel
    println("Pemberian nilai pertama pada varVariabel = "+varVariabel)
    varVariabel = 78 //assigning second value to varVariabel
    println("Pemberian nilai kedua pada varVariabel = "+varVariabel)

    //contoh kode menggunakan val
    val phi = 3.14
    println("Phi = " + phi)
    //phi = 2.5
    /*
    Ketika kita merubah nilai variabel val phi untuk kedua kalinya
    maka akan terdapat pesan kesalahan agar kita menggunakan var variabel.
     */

}